package paperrockscissors.enums;

/**
 * An enum class representing the possible outcomes of a round in this game.
 */
public enum RoundResult {
    /**
     * Indicates that the round was won.
     */
    WON,
    /**
     * Indicates that the round was lost.
     */
    LOST,
    /**
     * Indicates that the round was a tie.
     */
    TIE
}
